<h1>
Skalné obydlia
</h1>
<p class="text">
Dodnes sa traduje, že tunajšie skalné obydlia majú vlastne svoj pôvod v ochrane domáceho obyvateľstva pred nájazdmi Turkov v 16. a 17. storočí.
<br />
Keďže sa tu nachádza dobre spracovateľský usadený popol a prach – tufit a tuf a obec a jej okolie zaznamenali v období treťohôr bohatú sopečnú činnosť, naskytla sa jedinečná možnosť ako ich využiť. A tak si do usadenín Brhlovčania vyrúbali jedinečné obytné i hospodárske priestory. Kameň bol vhodným materiálom nielen na vytvorenie originálnych obydlí, ale poskytoval obyvateľstvu zdroj obživy.
<br />
V devätnástom storočí sa stalo kamenárstvo najdôležitejšou formou existencie tunajších ľudí, ktorí sa techniku ťažby a opracovania kameňa naučili od talianskych staviteľov miestneho kaštieľa.
</p>
<p class="text">
Expozícia pozostáva z murovaného domu z konca 19. storočia s datovaním 1932 a priestorov vysekaných do skaly v dvoch podlažiach. Na prízemí priestorov vysekaných do skaly je letná kuchyňa, komora a ukážky kamenárskych výrobkov a náradia. Na podlaží priestorov vysekaných do skaly je vyhliadková terasa a malá komôrka. Interiér je zariadený podľa funkcie jednotlivých miestností dobovým nábytkom a inventárom a dokumentuje zvláštnu formu ľudové staviteľstva a bývania od konca 19. storočia po 50. roky 20. storočia.
<br />
V roku 1994 <a href="http://www.muzeumlevice.sk/" target="blank">Tekovské múzeum v Leviciach</a> za expozíciu Skalné obydlia obdržala ocenenie Europa Nostra, ako prvá organizácia na pôde Slovenskej republiky.
</p>
